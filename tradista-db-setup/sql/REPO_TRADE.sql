CREATE table REPO_TRADE(
REPO_TRADE_ID BIGINT PRIMARY KEY, 
REPO_RATE DECIMAL(30,15),
MARGIN_RATE DECIMAL(30,15) NOT NULL,
INDEX_ID BIGINT,
INDEX_TENOR VARCHAR(20),
INDEX_OFFSET DECIMAL(30,15),
END_DATE DATE,
RIGHT_OF_SUBSTITUTION BOOLEAN,
RIGHT_OF_REUSE BOOLEAN,
CROSS_CURRENCY_COLLATERAL BOOLEAN,
TERMINABLE_ON_DEMAND BOOLEAN,
NOTICE_PERIOD BOOLEAN,
CONSTRAINT FK_REPO_TRADE_ID FOREIGN KEY (REPO_TRADE_ID) REFERENCES TRADE(ID),
CONSTRAINT FK_REPO_TRADE_INDEX_ID FOREIGN KEY (INDEX_ID) REFERENCES INDEX(ID));