CREATE table GCREPO_TRADE(
GCREPO_TRADE_ID BIGINT, 
REPO_RATE DECIMAL(30,15) NOT NULL,
MARGIN_RATE DECIMAL(30,15) NOT NULL,
INDEX_ID BIGINT,
INDEX_TENOR VARCHAR(20),
INDEX_OFFSET DECIMAL(30,15),
END_DATE DATE,
RIGHT_OF_SUBSTITUTION BOOLEAN,
RIGHT_OF_REUSE BOOLEAN,
CROSS_CURRENCY_COLLATERAL BOOLEAN,
TERMINABLE_ON_DEMAND BOOLEAN,
NOTICE_PERIOD BOOLEAN,
GCBASKET_ID BIGINT,
CONSTRAINT FK_GCREPO_TRADE_GCBASKET_ID FOREIGN KEY (GCBASKET_ID) REFERENCES GCBASKET(ID),
CONSTRAINT FK_GCREPO_TRADE_ID FOREIGN KEY (GCREPO_TRADE_ID) REFERENCES TRADE(ID),
CONSTRAINT FK_GCREPO_TRADE_INDEX_ID FOREIGN KEY (INDEX_ID) REFERENCES INDEX(ID));